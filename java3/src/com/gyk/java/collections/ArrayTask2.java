package com.gyk.java.collections;

public class ArrayTask2 {

    public static void main(String[] args) {
        int[] numbers = {
                40, 55, 63, 17, 22, 68, 89, 97, 89
        };

        for (int x:numbers) {
            System.out.print(x + " ");
        }
        System.out.print("\n");
        System.out.println("last line");
    }

}
